package e_oop;

public class Calculator {
	
	//메소드(5개)마다 2개의 파라미터
	//연산은 결과값을 리턴

	long plus(long a, long b){
		return a + b;
	}
	
	long minus(long a, long b){
		return a - b;
	}
	
	long multiple(long a, long b){
		return a * b;
	}
	
	long division(long a, long b){
		return a / b;
	}
	
	long remain(long a, long b){
		return a % b;
	}
	
}
